这个模型包含哪些要素呢？
1）首先是冰山上面露出海面的部分，这部分最容易观
测：包括知识和技能。
2）其次是冰山中间的部分：为什么说能力在冰山的中
间呢，因为它在海面上半隐半浮，有些能力比较容易
体现出来，比如说沟通能力；有些能力是隐性的，不
容易被外人看出来，甚至你自己都不知道自己是否拥
有这个能力，比如领导力。
3）最后是完全隐藏在海面之下的部分，这一部分不容
易被观测到，却对人的行为表现起到至关重要的作
用，它包括：价值观、性格和动机。